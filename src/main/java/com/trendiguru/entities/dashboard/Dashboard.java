package com.trendiguru.entities.dashboard;

public class Dashboard {
	private String kibanaDashboard;
	private String iconUrl;
	
	public Dashboard() {
	}

	public String getKibanaDashboard() {
		return kibanaDashboard;
	}

	public void setKibanaDashboard(String kibanaDashboard) {
		this.kibanaDashboard = kibanaDashboard;
	}

	public String getIconUrl() {
		return iconUrl;
	}

	public void setIconUrl(String iconUrl) {
		this.iconUrl = iconUrl;
	}
	
}
